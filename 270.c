#include <stdio.h>

int main() {
    // volatile сообщает компилятору, что переменная может измениться
    // извне (например, аппаратным прерыванием или другим потоком).
    // Поэтому компилятор не будет удалять или оптимизировать этот цикл.
    volatile int flag = 1; 
    
    // В реальной встроенной системе этот цикл ждал бы прерывания:
    // while(flag) { /* ожидание */ } 
    
    printf("Volatile переменная = %d\n", flag);
    return 0;
}

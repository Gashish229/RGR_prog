#include <stdio.h>

int main(void) {
    int arr[3] = {10, 20, 30};

    // Указатель строго на массив из 3 элементов типа int (скобки обязательны!)
    int (*ptr_to_array)[3] = &arr; 

    // Доступ: сначала разыменовываем указатель на массив, затем берем элемент
    printf("Элемент через указатель: %d\n", (*ptr_to_array)[1]); // Выведет 20
    return 0;
}
